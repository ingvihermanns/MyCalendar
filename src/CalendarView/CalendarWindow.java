    private void myCalendarPropertyChange(java.beans.PropertyChangeEvent evt) {//GEN-FIRST:event_myCalendarPropertyChange
        
        // Ef viðburði dagsetningar hefur verið breytt
        // er nýi viðburðurinn vistaður í skrá
        if(DiaryValues.setDateText(days, currentDate, vidburdur.getText())){
            saveToFile();
            System.out.println("Nýr atburður hefur verið vistaður");
        }
        
        // Uppfæra currentDate með nýju dagsetningunni
        currentDate = DiaryValues.dateToString(myCalendar.getDate());
        
        getCurrentEvent();
        
        // Uppfæra viðmótið á dagahnöppunum
        myCalendar.getDayChooser().updateUI();
        
        // Uppfæra viðmótið á deginum í dag
        myCalendar.getDayChooser().setDay(myCalendar.getDayChooser().getDay());
        
        System.out.println("Breytt hefur verið um dag");
    }//GEN-LAST:event_myCalendarPropertyChange

