        /**
     * Nær í viðburð fyrir núverandi dagsetningu og skrifar í vidburdur
     */
    private void getCurrentEvent(){
        
        // Ná í textann sem er geymdur fyrir dagsetninguna currentDate
        String currentEvent = DiaryValues.getDateText(days, currentDate);       
        // Setja textann í textasviðið
        vidburdur.setText(currentEvent);
    }
    
    /**
     * Keyrist þegar forriti er lokað
     * Sér til þess að atburður vistist
     * @param evt 
     */
    private void formWindowClosed(java.awt.event.WindowEvent evt) {                                  
        
        saveToFile();
        System.out.println("Forriti lokað");
    }                                 
    
    
    /**
     * Vistar value töfluna í skrá
     */
    private void saveToFile(){
        try {
            DiaryValues.setDateText(days, currentDate, vidburdur.getText()); 
            DiaryValues.saveDays(days);
        }
        catch(java.io.IOException e){
            System.out.println("There was an error saving your data");
        }
    }
    
    
